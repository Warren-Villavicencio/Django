# Ejemplo de uso de variables numéricas y de texto
# Este código calcula el perímetro de un círculo

radio = 5.0  # Variable de tipo float para el radio
pi = 3.14159  # Constante matemática pi
diametro = 2 * radio  # Calculamos el diámetro
perimetro = pi * diametro  # Calculamos el perímetro

print("El perímetro del círculo es:", perimetro)