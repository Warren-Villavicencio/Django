# Ejemplo de uso de listas y diccionarios
# Este código crea una lista de frutas y un diccionario con información de una persona

frutas = ["manzana", "banana", "pera"]  # Lista de frutas
persona = {"nombre": "Juan", "edad": 30, "ciudad": "Madrid"}  # Diccionario con información personal

# Accediendo a elementos de la lista y el diccionario
print("La segunda fruta es:", frutas[1])
print("La edad de Juan es:", persona["edad"])